void Camera_Set_Reg(XIic *IicInstancePtr, struct reginfo *regarray) {
    int i = 0;
    while (regarray[i].reg != SEQUENCE_END) {
    	IicWriteData16(IicInstancePtr, regarray[i].reg, &(regarray[i].val), 1);
		i++;
	}
}

